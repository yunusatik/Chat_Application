    private void txtPanel_GirdiKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPanel_GirdiKeyPressed
        //buraya gelince coktan degismis oluyor

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            ArrayList ids = new ArrayList<>();
            ids.add(ThisPrivate.ID);
            ids.add(ThisPrivate.friendID);
            ArrayList nameInfo = new ArrayList<>();
            Message msg = new Message(Message.Message_Type.SendPrivateMessageCheck);
            nameInfo.add(txt_My_Name.getText());        //isim
            nameInfo.add(color);                        //color
            nameInfo.add(txtPanel_Girdi.getText());     //girdi
            nameInfo.add(emoji);                        //emoji
            nameInfo.add(online_clients_message);       //friend name
            nameInfo.add(show_ID);                   //showID
            nameInfo.add(ids);                          //friendID
            nameInfo.add(lbl_Room_Name.getText());      //Room Name
            nameInfo.add(0);
            msg.content = nameInfo;
            Client.Send(msg);
            emoji = new ArrayList();
            emoji.clear();
            txtPanel_Girdi.setText("");
        }
    }//GEN-LAST:event_txtPanel_GirdiKeyPressed

    private void lbl_File_SendMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_File_SendMousePressed

        File[] fileToSend = new File[1];
        JFileChooser jFileChooser = new JFileChooser();
        jFileChooser.setDialogTitle("Choose a file to send.");

        if (jFileChooser.showOpenDialog(ThisPrivate) == JFileChooser.APPROVE_OPTION) {
            try {
                ArrayList id = new ArrayList<>();
                id.add(ThisPrivate.friendID);
                ArrayList fileInfo = new ArrayList<>();
                Message msg3 = new Message(Message.Message_Type.SendFilePrivateCheck);
                fileToSend[0] = jFileChooser.getSelectedFile();
                fileInfo.add(fileToSend[0].getName());              //0 path

                byte[] content = Files.readAllBytes(fileToSend[0].toPath());
                fileInfo.add(content);                              //1 data

                fileInfo.add(txt_My_Name.getText());                //2 my name
                fileInfo.add(show_ID);                           //3 showID
                fileInfo.add(private_online_clients);               //4 friend names
                fileInfo.add(ThisPrivate.friendID);                 //5 App_ID
                fileInfo.add(id);                                   //6 App_ID
                fileInfo.add(lbl_Room_Name.getText());              //7 room name

                msg3.content = fileInfo;    //path,data,myname,showID,friendname,App_ID,App_ID
                Client.Send(msg3);

            } catch (IOException ex) {
                System.out.println("Dosya GÃ¶ndermede Hata! (App.Java)");
            }
        }

    }//GEN-LAST:event_lbl_File_SendMousePressed

